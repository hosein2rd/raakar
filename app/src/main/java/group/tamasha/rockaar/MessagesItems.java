package group.tamasha.rockaar;

public class MessagesItems
{
    public int green;
    public String name;
    public String title;
    public String text;
    public String date;
    public String num;

    MessagesItems(int green, String name, String title, String text, String date, String num)
    {
        this.green = green;
        this.name = name;
        this.title = title;
        this.text = text;
        this.date = date;
        this.num = num;
    }
}
